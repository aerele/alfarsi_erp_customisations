@frappe.whitelist()
def update_rec_items_batch(item_names, rec_items, group):
    rec_items = list(set(rec_items))

    for item_name in item_names:
        try:
            doc = frappe.get_doc("Website Item", item_name)
            doc.recommended_items = []

            for rec_item in rec_items:
                if rec_item != item_name:
                    doc.append("recommended_items", {
                        "website_item": rec_item
                    })

            doc.save()
            frappe.db.commit()
        except Exception as e:
            frappe.log_error(
                message=f"Error updating item {item_name} - {group}: {e}",
                title=f"Error updating item {item_name} - {group}"
            )